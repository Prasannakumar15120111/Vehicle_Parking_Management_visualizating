#Vehicle parking project 

create database vehicleparking;

use vehicleparking;

CREATE TABLE User (
    UserID INT PRIMARY KEY AUTO_INCREMENT,
    Username VARCHAR(50) NOT NULL,
    Password VARCHAR(100) NOT NULL,
    Email VARCHAR(100),
    PhoneNumber VARCHAR(15),
    UserType ENUM('Admin', 'RegularUser')
);

CREATE TABLE Vehicle (
    VehicleID INT PRIMARY KEY AUTO_INCREMENT,
    UserID INT,
    LicensePlate VARCHAR(20) NOT NULL,
    VehicleType VARCHAR(20),
    Brand VARCHAR(50),
    Model VARCHAR(50),
    Color VARCHAR(20),
    FOREIGN KEY (UserID) REFERENCES User(UserID)
);

CREATE TABLE ParkingLot (
    ParkingLotID INT PRIMARY KEY AUTO_INCREMENT,
    Name VARCHAR(100) NOT NULL,
    Location VARCHAR(255),
    Capacity INT,
    OpenHours TIME,
    CloseHours TIME
);

CREATE TABLE ParkingSlot (
    SlotID INT PRIMARY KEY AUTO_INCREMENT,
    ParkingLotID INT,
    SlotNumber VARCHAR(10) NOT NULL,
    SlotSize ENUM('Compact', 'Regular', 'Large'),
    Status ENUM('Available', 'Occupied', 'Reserved'),
    FOREIGN KEY (ParkingLotID) REFERENCES ParkingLot(ParkingLotID)
);

CREATE TABLE Reservation (
    ReservationID INT PRIMARY KEY AUTO_INCREMENT,
    UserID INT,
    SlotID INT,
    VehicleID INT,
    ReservationTime DATETIME,
    StartTime DATETIME,
    EndTime DATETIME,
    FOREIGN KEY (UserID) REFERENCES User(UserID),
    FOREIGN KEY (SlotID) REFERENCES ParkingSlot(SlotID),
    FOREIGN KEY (VehicleID) REFERENCES Vehicle(VehicleID)
);

CREATE TABLE Payment (
    PaymentID INT PRIMARY KEY AUTO_INCREMENT,
    ReservationID INT,
    Amount DECIMAL(10, 2) NOT NULL,
    PaymentMethod ENUM('Credit Card', 'Debit Card', 'Cash', 'Online'),
    PaymentTime DATETIME,
    PaymentStatus ENUM('Pending', 'Completed', 'Failed'),
    FOREIGN KEY (ReservationID) REFERENCES Reservation(ReservationID)
);



